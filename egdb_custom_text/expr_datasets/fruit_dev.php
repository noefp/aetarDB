      <p style="text-align: justify;">
         Plants of <i>Aethionema arabicum</i> were grown from accession no. ES1020, in Levington compost with added horticultural grade sand (F2 + S), under long-day conditions (16 h light/20°C and 8 h dark/18°C) in a greenhouse. The  days after pollination (DAP) were defined phenotypically as the time at which the flowers open (anthesis) and the four long stamens extend over the gynoecium. Floral buds (0 DAP), flowers at anthesis (1 DAP) and immature fruits at their full length (30 DAP, before the onset of yellowing and drying) were harvested from second-order branches of plants that grew undisturbed (IND) or from the main branch of plants where side branches were constantly removed during development (DEH). Four biological replicates were sequenced using 50-bp single-end mode on Illumina® HiSeq 2000, which were processed, analyzed and normalized to RPKM.
      </p>
