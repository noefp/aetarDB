<p>
  
  RNA-seq of <i>Aethionema arabicum</i> TUR fruit development, from flower buds to immature fruits at their full length.
  There are two fruit types: indehiscent fruits (IND) and dehiscent fruits (DEH).
</p>
<h4>Experimental Conditions (4 replicates):</h4>
  <ul>
    <li>buds cut (DEH)</li>
    <li>buds uncut (IND)</li>
    <li>flowers cut (DEH)</li>
    <li>flowers uncut (IND)</li>
    <li>fruits cut (DEH)</li>
    <li>fruits uncut (IND)</li>
  </ul>


