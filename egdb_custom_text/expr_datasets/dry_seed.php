<p>
  RNA-seq of <i>Aethionema arabicum</i> dry seeds. There are two seed types: larger non mucilaginous seeds (NM) from indehiscent fruits (IND) and smaller mucilaginous seeds (M) from dehiscent fruits (DEH).
</p>
<h4>Experimental Conditions (2 replicates):</h4>
  <ul>
    <li>NM: Non mucilaginous dry seeds</li>
    <li>M: mucilaginous dry seeds</li>
  </ul>

