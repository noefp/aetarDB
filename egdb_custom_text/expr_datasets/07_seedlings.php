<p>
  RNA-seq of <i>Aethionema arabicum</i> (Turkey ecotype) germinating seeds, germinated seeds and early seedlings derived from natural M+ seed and IND fruit diaspores and bare M- seed (through pericarp removal). Germinating seeds imbibed at 9°C were sampled at the physiological stages of 1% germination (T1%) and 100% germination (T100%). Root and shoot tissues of seedlings were sampled 72 hours and 240 hours after post-germinative growth at 30°C (A). 
</p>
