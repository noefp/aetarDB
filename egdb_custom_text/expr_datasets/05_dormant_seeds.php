<p style="text-align: justify;">
  The seeds of one <i>Ae. arabicum</i> accession from Cyprus (CYP) germinates only in the dark, while light inhibits the germination. This phenotype if also called photoinhibition, or negative photoblasty. Extended light illumination also induces secondary seed dormancy, when the seeds are not germinating even when they are transferred back to the optimal dark condition. To investigate the transition from photoinhibition to photodormancy, transcriptome of CYP seeds where compared under the conditions when they are inhibited but not yet dormant (1 day light), or when the light induced secondary is already established ( 7 or 14 days light). A further dormant sample ( 7 days light+7 days dark) is used to differentiate those expressional changes, which are due to the light stress from those which are associated with secondary dormancy.
